    private void OnClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OnClick
        if(evt.getSource() == jbtnInDat){
            Datos.setBounds(this.getX() + 100, this.getY() + 5, Datos.getWidth(), Datos.getHeight());
            Datos.setVisible(true);
            if (bLimpiar)
                tablaRestricciones();
            bLimpiar = false;
            jbtnSolPasoAPaso.setEnabled(false);
            iNoRest = modRest.getRowCount()-1;
        } else if(evt.getSource() == jbtnInicio){
            new Inicio().setVisible(true);
            this.dispose();
        } else if(evt.getSource() == jbtnSalir)
            System.exit(0);
        else if(evt.getSource() == jbtnOk)
            tablaRestricciones();
        else if(evt.getSource() == jbtnContinuar){
            if (!validar()){
                TablaSimplex.TablaSolucion(iX, iNoRest, modRest, jtblTabla);
                modSolu = (DefaultTableModel) jtblTabla.getModel();
                MetodoSimplex.calcularZj(modSolu);
                MetodoSimplex.calcularCj_Zj(modSolu);
                if (jcbMaxMin.getSelectedIndex() == 0)
                    MetodoSimplex.mayorFilaCj_Zj(modSolu);
                else
                    MetodoSimplex.menorFilaCj_Zj(modSolu);
                MetodoSimplex.CalcularXn_Mayor(modSolu);
                MetodoSimplex.menor(modSolu);
                jbtnSolucion.setEnabled(true);
                jbtnSolPasoAPaso.setEnabled(true);
                Datos.setVisible(false);
                MetodoSimplex.numeroTablones = 1;
                jtxtarModMat.setText(null);
                jlblFila.setText("Fila: " + (filaMenor - 1));
                jlblColumna.setText("Columna: " + (columnaMayor));
                jlblPivote.setText("Pivote: " + pivote);
                jlblPivote.setText("Pivote: " + AproximarNumero.valorAproximado
        (String.valueOf(modSolu.getValueAt(filaMenor, columnaMayor))));
                ecuaciones();
            }
        } else if(evt.getSource() == jbtnSolucion){
            while(!todosMenor  && filaMenor > 0){
                Click(evt);
            }
            jbtnSolucion.setEnabled(false);
            limpiarUltimaCelda();
        }
    }//GEN-LAST:event_OnClick

    private void jspRestStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jspRestStateChanged
        int row = Integer.parseInt(jspRest.getValue().toString());
        String g[] = new String[iX];
        if(row > iNoRest){
            for(int i = iNoRest + 1; i <= row; i++){
                //g[0] = "Rest " + i;
                modRest.addRow(g);
                modRest.setValueAt("<=", i, modRest.getColumnCount() - 2);
            }
            iNoRest = row;
        } else if(row < iNoRest){
            while (iNoRest != row){
                modRest.removeRow(iNoRest);
                iNoRest--;
            }
        }
    }//GEN-LAST:event_jspRestStateChanged

