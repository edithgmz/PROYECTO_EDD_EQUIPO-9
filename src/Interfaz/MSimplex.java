    private void Click(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Click
        if(!MetodoSimplex.todosMenor){
                if(MetodoSimplex.filaMenor <= 0)
                    JOptionPane.showMessageDialog(this, "No se ha podido encontrar una solución al sistema.");
                else{
                    MetodoSimplex.Convertir0Pivote(modSolu);
                    MetodoSimplex.Gauss(modSolu);
                    MetodoSimplex.calcularZj(modSolu);
                    MetodoSimplex.calcularCj_Zj(modSolu);
                    if(jcbMaxMin.getSelectedItem().toString().equalsIgnoreCase("Maximizar"))
                        MetodoSimplex.mayorFilaCj_Zj(modSolu);
                    else if(jcbMaxMin.getSelectedItem().toString().equalsIgnoreCase("Minimizar"))
                        MetodoSimplex.menorFilaCj_Zj(modSolu);
                    MetodoSimplex.CalcularXn_Mayor(modSolu);
                    MetodoSimplex.menor(modSolu);
                }
            }
            if(MetodoSimplex.todosMenor){
                jbtnSolPasoAPaso.setEnabled(false);
                GetX.mostar(modSolu, iX);
                limpiarUltimaCelda();
            }
            jlblFila.setText("Fila: " + (MetodoSimplex.filaMenor - 1));
            jlblColumna.setText("Columna: " + (MetodoSimplex.columnaMayor));
            jlblPivote.setText("Pivote: " + AproximarNumero.valorAproximado(String.valueOf
        (modSolu.getValueAt(MetodoSimplex.filaMenor, MetodoSimplex.columnaMayor))));
    }//GEN-LAST:event_Click

    private void tablaRestricciones(){
        iX = Integer.parseInt(String.valueOf(jspVar.getValue()));
        iNoRest = Integer.parseInt(String.valueOf(jspRest.getValue()));
        Object oCabeza[] = new String[iX + 3];
        oCabeza[0] = ("");
        for(int i = 1; i <= iX; i++) {
            oCabeza[i] = ("X" + i);
        }
        oCabeza[iX + 1] = ("");
        oCabeza[iX + 2] = ("");
        modRest = new DefaultTableModel(oCabeza, 0){
            public boolean isCellEditable(int row, int column){
                if(column == 0)
                    return false;
                if(row == 0 && column >= modRest.getColumnCount() - 2)
                    return false;
                return true;
            }
        };
        jtblRest.setModel(modRest);
        String row[] = new String[iX + 3];
        for (int i = 0; i < iNoRest + 1; i++){
            if (i == 0)
                row[0] = new String("     Z =");
            else{
                row[0] = new String("Rest. " + (i));
                row[modRest.getColumnCount() - 2] = "<=";
            }
            modRest.addRow(row);
        }
        agregarOpciones(modRest.getColumnCount(), jtblRest);
    }
    private void ecuaciones(){
        jtxtarModMat.setText(null);
        int row = modRest.getRowCount();
        int column = modRest.getColumnCount();
        jtxtarModMat.append(" Z = ");
        for(int i = 1; i <column-2; i++) {
            if(Double.parseDouble(String.valueOf(modRest.getValueAt(0, i))) >= 0) 
                jtxtarModMat.append(" +" + " " + modRest.getValueAt(0, i) + "X" + i);
            else
                jtxtarModMat.append(" " + modRest.getValueAt(0, i) + " X" + i);
        }
        jtxtarModMat.append(" \n\n");
        for(int i = 1; i < row; i++) {
            jtxtarModMat.append(" ");
            for(int j = 1; j < column; j++){
                if(j == 1)
                    jtxtarModMat.append("" + modRest.getValueAt(i, j) + "X" + j);
                else if(j == column - 2)
                    jtxtarModMat.append(" " + modRest.getValueAt(i, j));
                else{
                    if(j >= column - 2)
                        jtxtarModMat.append(" " + modRest.getValueAt(i, j));
                    else{
                        if(Double.parseDouble(String.valueOf(modRest.getValueAt(i, j))) >= 0)
                            jtxtarModMat.append(" +" + " " + modRest.getValueAt(i, j) + "X" + j);
                        else
                            jtxtarModMat.append(" " + modRest.getValueAt(i, j) + " X" + j);
                    }
                }
            }
            jtxtarModMat.append("\n");
        }
    }
    private void agregarOpciones(int a, JTable jtRest){
        String[] Condicion = {"<=", ">=", "=",">","<"};
        JComboBox jcb = new JComboBox(Condicion);
        TableColumn tc = jtRest.getColumnModel().getColumn(a - 2);
        TableCellEditor tce = new DefaultCellEditor(jcb);
        tc.setCellEditor(tce);
        jtRest.getColumnModel().getColumn(0).setPreferredWidth(50);
        jtRest.getColumnModel().getColumn(a - 1).setPreferredWidth(80);
        jtRest.getColumnModel().getColumn(a - 2).setPreferredWidth(30);
        for (int i = 1; i <= a - 2; i++){
            jtRest.getColumnModel().getColumn(0).setPreferredWidth(80);
        }
        jtRest.setRowHeight(25);
    }
    private boolean validar() {
        String texto;
        int row = modRest.getRowCount();
        int column = modRest.getColumnCount();
        for(int i = 0; i < row; i++){
            for(int j = 1; j < column; j++){
                if(i == 0 && j >= column - 2)
                    break;
                else if(j != column - 2){
                    texto = String.valueOf(modRest.getValueAt(i, j));
                    if (!texto.matches("([+-]{1})?[0-9]+(\\.[0-9]+)?([Ee]{1}([-]{1})?[1-9])?")) {
                        JOptionPane.showMessageDialog(this, "Faltan o hay datos incorrectos,\nasegúrese de no escribir espacios en \nblanco o los decimales con coma.");
                        return true;
                    }
                }
            }
        }
        return false;
    }
    public void limpiarUltimaCelda(){
        for(int i = 0; i < jtblTabla.getRowCount(); i++) {
            jtblTabla.setValueAt("", i, jtblTabla.getColumnCount()-1);
        }
    }
    
